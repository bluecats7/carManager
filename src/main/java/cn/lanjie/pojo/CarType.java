package cn.lanjie.pojo;


public class CarType {

  private long id;
  private String carType;
  private String pic;
  private long passengersNum;
  private long loadWeight;
  private String size;
  private String costInfo;
  private double commission;
  private double rentalPrice;
  private double mileageRent;
  private double mileagePrice;
  private double durationPrice;
  private double longDistance;
  private double standard;
  private String remark;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getCarType() {
    return carType;
  }

  public void setCarType(String carType) {
    this.carType = carType;
  }


  public String getPic() {
    return pic;
  }

  public void setPic(String pic) {
    this.pic = pic;
  }


  public long getPassengersNum() {
    return passengersNum;
  }

  public void setPassengersNum(long passengersNum) {
    this.passengersNum = passengersNum;
  }


  public long getLoadWeight() {
    return loadWeight;
  }

  public void setLoadWeight(long loadWeight) {
    this.loadWeight = loadWeight;
  }


  public String getSize() {
    return size;
  }

  public void setSize(String size) {
    this.size = size;
  }


  public String getCostInfo() {
    return costInfo;
  }

  public void setCostInfo(String costInfo) {
    this.costInfo = costInfo;
  }


  public double getCommission() {
    return commission;
  }

  public void setCommission(double commission) {
    this.commission = commission;
  }


  public double getRentalPrice() {
    return rentalPrice;
  }

  public void setRentalPrice(double rentalPrice) {
    this.rentalPrice = rentalPrice;
  }


  public double getMileageRent() {
    return mileageRent;
  }

  public void setMileageRent(double mileageRent) {
    this.mileageRent = mileageRent;
  }


  public double getMileagePrice() {
    return mileagePrice;
  }

  public void setMileagePrice(double mileagePrice) {
    this.mileagePrice = mileagePrice;
  }


  public double getDurationPrice() {
    return durationPrice;
  }

  public void setDurationPrice(double durationPrice) {
    this.durationPrice = durationPrice;
  }


  public double getLongDistance() {
    return longDistance;
  }

  public void setLongDistance(double longDistance) {
    this.longDistance = longDistance;
  }


  public double getStandard() {
    return standard;
  }

  public void setStandard(double standard) {
    this.standard = standard;
  }


  public String getRemark() {
    return remark;
  }

  public void setRemark(String remark) {
    this.remark = remark;
  }

}
