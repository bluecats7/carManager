package cn.lanjie.pojo;


public class FastCar {

  private long id;
  private long fid;
  private long cid;
  private double mile;
  private long time;
  private double rent;
  private double mileMoney;
  private double timeMoney;
  private double distanceMoney;
  private double save;
  private double pay;
  private long state;
  private java.sql.Date create;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getFid() {
    return fid;
  }

  public void setFid(long fid) {
    this.fid = fid;
  }


  public long getCid() {
    return cid;
  }

  public void setCid(long cid) {
    this.cid = cid;
  }


  public double getMile() {
    return mile;
  }

  public void setMile(double mile) {
    this.mile = mile;
  }


  public long getTime() {
    return time;
  }

  public void setTime(long time) {
    this.time = time;
  }


  public double getRent() {
    return rent;
  }

  public void setRent(double rent) {
    this.rent = rent;
  }


  public double getMileMoney() {
    return mileMoney;
  }

  public void setMileMoney(double mileMoney) {
    this.mileMoney = mileMoney;
  }


  public double getTimeMoney() {
    return timeMoney;
  }

  public void setTimeMoney(double timeMoney) {
    this.timeMoney = timeMoney;
  }


  public double getDistanceMoney() {
    return distanceMoney;
  }

  public void setDistanceMoney(double distanceMoney) {
    this.distanceMoney = distanceMoney;
  }


  public double getSave() {
    return save;
  }

  public void setSave(double save) {
    this.save = save;
  }


  public double getPay() {
    return pay;
  }

  public void setPay(double pay) {
    this.pay = pay;
  }


  public long getState() {
    return state;
  }

  public void setState(long state) {
    this.state = state;
  }


  public java.sql.Date getCreate() {
    return create;
  }

  public void setCreate(java.sql.Date create) {
    this.create = create;
  }

}
